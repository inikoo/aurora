<p>
An organization would fail without customers; equally, suppliers are integral to its success. Most organizations have recognized the importance of managing customer relationships effectively, and consequently there is a strong focus on strategies and processes to facilitate this. This is aided by mature technology in the form of customer relationship management (CRM) software. Until recently, however, supplier relationship management (SRM) has been the forgotten frontier.
</p>
<p>
Supplier relationship management (SRM) is a discipline of working collaboratively with those suppliers that are vital to the success of your organization, to maximize the potential value of those relationships.
Why does this matter? Studies  of SRM implementations indicate their estimated savings are 3-5 per cent. Conversely, a lack of focus on SRM presents opportunities for suppliers to exploit this state of affairs via the traditional tactic of "divide and conquer" among the various business functions of an organization. Steven Pink, head of provider relations and procurement at UK private healthcare firm Bupa Insurance, believes that "effective supplier management focuses on new product development, a better customer experience, improved organizational capability and reduced total cost of ownership. All of these are key strategic outputs for an organization."
</p>
<p>
To capture the benefits and avoid the pitfalls, suppliers that are deemed strategic to the organization's operation should be managed and nurtured using an SRM process. Examples include suppliers with whom the organization spends significant amounts of money, those that provide critical products or services, or those with a record of innovation.
</p>
<p>
A number of areas can be improved through diligent supplier relationship management.
</p>
<p>
They include:
<ul>
    <li> The ability to model costs more accurately.</li>
    <li> Utilisation of cross-organisational teams.</li>
    <li> Reduction in the impact of price fluctuations on cost structures.</li>
    <li> Early supplier involvement in product and service development.</li>
    <li> Transfer of knowledge through the supply chain.</li>
    <li> Planning and design synergy.</li>
    <li> Use of metrics to drive change for both organisations.</li>
    <li> Improved risk management and continuity of supply.</li>
    <li> Access to, and speed of, innovation.</li>
    <li> Implementation of a effective Procurement</li>
</ul>
</p>
<p>
Procurement is the acquisition of appropriate goods and/or services at the best possible total cost of ownership to meet the needs of the purchaser in terms of quality and quantity, time, and location. Corporations and public bodies often define processes intended to promote fair and open competition for their business while minimizing exposure to fraud and collusion.
</p>